<h4>Coming to Brno</h4>
<p style="text-align: justify;">
	There are three main airports near to Brno: <a href="http://tracker.flightview.com/customersetup/viennaairport/routemapper/?utm_source=Resieziele&utm_medium=Banner&utm_term=OAG&utm_campaign=OAG">Wien</a> (A), <a href="http://www.prg.aero/en/flight-info/route-map-and-timetable/">Prague</a> (CZ), <a href="https://www.skyscanner.net/inspire/map?outboundDate=2017-02-20&outboundPlace=BTS&preferDirects=false">Bratislava</a> (SK). All three serve EU and extra-EU routes. <br>
	&nbsp; From there, you can get to Brno in two ways: by train and by bus. <a href="https://www.czech-transport.com">Trains</a> to Brno regularly start from Wien central station and Prague central station.  A local bus company <a href="https://www.regiojet.com/en">RegioJet</a> offers cheap, reliable and comfortable connections from Wien airport to Brno. There are also smaller airports: <a href="http://www.brno-airport.cz/en/flight-information/destination-map/">Brno</a> itself, Pardubice, Ostrava.
</p>

